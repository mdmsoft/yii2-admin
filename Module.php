<?php

namespace mdm\admin;

use Yii;
use yii\helpers\Inflector;

/**
 * GUI manager for RBAC.
 * 
 * Use [[\yii\base\Module::$controllerMap]] to change property of controller. 
 * To change listed menu, use property [[$menus]].
 * 
 * ~~~
 * 'layout' => 'left-menu', // default to null mean use application layout.
 * 'controllerMap' => [
 *     'assignment' => [
 *         'class' => 'mdm\admin\controllers\AssignmentController',
 *         'userClassName' => 'app\models\User',
 *         'idField' => 'id'
 *     ]
 * ],
 * 'menus' => [
 *     'assignment' => [
 *         'label' => 'Grand Access' // change label
 *     ],
 *     'route' => null, // disable menu
 * ],
 * ~~~
 * 
 * @property string $mainLayout Main layout using for module. Default to layout of parent module.
 * Its used when `layout` set to 'left-menu', 'right-menu' or 'top-menu'.
 * @property array $menus List avalible menu of module.
 * It generated by module items .
 * 
 * @author Misbahul D Munir <misbahuldmunir@gmail.com>
 * @since 1.0
 */
class Module extends \yii\base\Module
{
    /**
     * @inheritdoc
     */
    public $defaultRoute = 'assignment';

    /**
     * @var array 
     * @see [[items]]
     */
    private $_menus;

    /**
     * @var string 
     */
    private $_mainLayout;

    /**
     * Get main layout of 'left-menu' and 'right-menu' layout.
     * @return string
     */
    public function getMainLayout()
    {
        if ($this->_mainLayout === null) {
            $module = $this->module;
            while ($module !== null && $module->layout === null) {
                $module = $module->module;
            }
            if ($module !== null && is_string($module->layout)) {
                $layout = $module->layout;
            }
            if (isset($layout)) {
                if (strncmp($layout, '@', 1) === 0) {
                    $file = Yii::getAlias($layout);
                } elseif (strncmp($layout, '/', 1) === 0) {
                    $file = Yii::$app->getLayoutPath() . DIRECTORY_SEPARATOR . substr($layout, 1);
                } else {
                    $file = $module->getLayoutPath() . DIRECTORY_SEPARATOR . $layout;
                }
            } else {
                $file = Yii::getAlias('@app/views/layouts/main');
            }

            if (pathinfo($file, PATHINFO_EXTENSION) !== '') {
                return $this->_mainLayout = $file;
            }
            $view = Yii::$app->getView();
            $path = $file . '.' . $view->defaultExtension;
            if ($view->defaultExtension !== 'php' && !is_file($path)) {
                $path = $file . '.php';
            }
            return $this->_mainLayout = $path;
        }
        return $this->_mainLayout;
    }

    /**
     * Set main layout of 'left-menu' and 'right-menu' layout.
     * @param string $layout
     */
    public function setMainLayout($layout)
    {
        $file = Yii::getAlias($layout);
        $view = Yii::$app->getView();
        $path = $file . '.' . $view->defaultExtension;
        if ($view->defaultExtension !== 'php' && !is_file($path)) {
            $path = $file . '.php';
        }
        $this->_mainLayout = $path;
    }

    /**
     * Get core menu
     * @return array
     */
    protected function getCoreMenus()
    {
        $mid = '/' . $this->getUniqueId() . '/';
        $ids = ['assignment', 'role', 'permission', 'route', 'rule', 'menu'];
        $config = components\Configs::instance();
        $result = [];
        foreach ($ids as $id) {
            if ($id !== 'menu' || ($config->db !== null && $config->db->schema->getTableSchema($config->menuTable) !== null)) {
                $result[$id] = ['label' => Inflector::humanize($id), 'url' => [$mid . $id]];
            }
        }
        foreach (array_keys($this->controllerMap) as $id) {
            $result[$id] = ['label' => Inflector::humanize($id), 'url' => [$mid . $id]];
        }
        return $result;
    }

    /**
     * Get avalible menu.
     * @return array
     */
    public function getMenus()
    {
        if ($this->_menus === null) {
            return $this->_menus = $this->getCoreMenus();
        }
        return $this->_menus;
    }

    /**
     * Set or add avalible menu.
     * @param array $menus
     */
    public function setMenus($menus)
    {
        $mid = '/' . $this->getUniqueId() . '/';
        $this->_menus = $this->getMenus();
        foreach ($menus as $id => $value) {
            if (empty($value)) {
                unset($this->_menus[$id]);
            } else {
                $this->_menus[$id] = isset($this->_menus[$id]) ? array_merge($this->_menus[$id], $value) : $value;
                if (!isset($this->_menus[$id]['url'])) {
                    $this->_menus[$id]['url'] = [$mid . $id];
                }
            }
        }
    }
}